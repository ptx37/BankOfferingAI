type TranslationKey = keyof typeof dict.en;

export function useTranslation() {
  const lang: Lang =
    (typeof window !== 'undefined' ? (localStorage.getItem('lang') as Lang) : null) ?? 'en';

  function t(key: TranslationKey, vars?: Record<string, string | number>): string {
    const map = (dict[lang] ?? dict.en) as Record<string, string>;
    let str = map[key] ?? (dict.en as Record<string, string>)[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, String(v));
      });
    }
    return str;
  }

  return { t, lang };
}
